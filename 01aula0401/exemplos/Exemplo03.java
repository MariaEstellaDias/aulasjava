public class Exemplo03 {
    public static void main(String[] args) {
        // declaração ou criação de variáveis
        // [TIPO] nome;
        int numero = 0; // warning - linha em amarelo

        numero = 15; // atribuição
        numero = 7; // variável pega o último valor

        System.out.println("A variável vale " + numero);

        numero = 10 + 5;

        System.out.println("10 + 5 = " + numero);

        System.out.println("Resultado = " + (numero - 5));

    }
}
